"""Dictionary"""

# empty dictionary
MY_DICT = {}

# dictionary with integer keys
MY_DICT = {1: 'goole', 2: 'yahoo'}

# dictionary with mixed keys
MY_DICT = {'lang': 'Python', 1: [3]}

# from sequence having each item as a pair
MY_DICT = dict([(1,'goole'), (2,'yahoo')])
print(MY_DICT)
# {1: 'goole', 2: 'yahoo'}